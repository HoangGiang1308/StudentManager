    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        StringBuilder str = new StringBuilder();
        Datavalidate.validate(txtstudentID, str, "Hãy Nhập mã sinh viên");
        Datavalidate.validate(txtenglish, str, "Hãy Nhập Điểm TA");
        Datavalidate.validate(txtgdtc, str, "Hãy Nhập Điểm GDTC");
        Datavalidate.validate(txtit, str, "Hãy Nhập Điểm Tin");
        if(str.length()>0){
            Message.showErrordialog(parentForm, str.toString(), "Lỗi");
            return;
        }
        try {
            bangdiem bd = new bangdiem();
            bd.setMASV(txtstudentID.getText());
            bd.setTA(Float.parseFloat(txtenglish.getText()));
            bd.setTin(Float.parseFloat(txtit.getText()));
            bd.setGDTC(Float.parseFloat(txtgdtc.getText()));
            bangdiemDAO dao = new bangdiemDAO();
            if(dao.insert(bd)){
                Message.showmessagedialog(parentForm, "lưu thành công", "Thông báo");
                loadbangdiem();
            }else{
                Message.showmessagedialog(parentForm, "lưu thất bại", "Thông báo");
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            Message.showErrordialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        StringBuilder str = new StringBuilder();
        Datavalidate.validate(txtstudentID, str, "Hãy Nhập mã sinh viên");
        if(str.length()>0){
            Message.showErrordialog(parentForm, str.toString(), "lỗi");
        }
        
        if(Message.showconfirmdialog(parentForm, "bạn có chắc muốn xóa sinh viên hay không ?", "Lưu ý") == JOptionPane.NO_OPTION){
            return;
        }
        try {
            bangdiemDAO dao = new bangdiemDAO();
            if(dao.delete(txtstudentID.getText())){
                Message.showmessagedialog(parentForm, "Sinh viên đã được xóa", "Thông báo");
                btnnewActionPerformed(evt);
                loadbangdiem();
            }else{
                Message.showconfirmdialog(parentForm, "Sinh viên không được xóa", "Lưu Ý");
            }
        } catch (Exception e) {
        e.printStackTrace();
            Message.showErrordialog(parentForm,e.getMessage(), "lỗi");
        }
    }//GEN-LAST:event_btndeleteActionPerformed

