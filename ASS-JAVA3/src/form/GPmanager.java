    private void loadbangdiem(){
        try {
            bangdiemDAO dao = new bangdiemDAO();
            List<bangdiem> list = dao.findtop(3);
            tblmodel.setRowCount(0);
            for(bangdiem bd :list){
                tblmodel.addRow(new Object[]{
                    bd.getMASV(),bd.getTA(),bd.getTin(),bd.getGDTC(),
                    (bd.getTA()+ bd.getTin()+ bd.getGDTC())/3
                });
            }
            tblmodel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            Message.showmessagedialog(parentForm,e.getMessage(),"lỗi");
        }
    }
