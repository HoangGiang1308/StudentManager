CREATE TABLE grade(
ID INT PRIMARY KEY NOT NULL,
MASV NVARCHAR(50),
TA FLOAT,
tin FLOAT,
GDTC FLOAT,
CONSTRAINT fk_st_gr FOREIGN KEY (MASV) REFERENCES student(MASV)
);
