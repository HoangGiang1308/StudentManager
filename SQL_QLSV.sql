MASV NVARCHAR(50) PRIMARY KEY NOT NULL,
hoten NVARCHAR(50),
email NVARCHAR(50),
SDT NVARCHAR(50),
Gioitinh BIT,
diachi NVARCHAR(50),
hinh NVARCHAR(50)
);
CREATE TABLE users(
username NVARCHAR(50),
password NVARCHAR(50),
role NVARCHAR(50)
);
